from win32api import GetSystemMetrics

##########################################################
# SCREEN
##########################################################
# getResolution ============================================
## Return the screen resolution.
# @return Tuple of Integer - screen width and height.
def getResolution():

    width = GetSystemMetrics(0)
    height = GetSystemMetrics(1)

    return width, height


